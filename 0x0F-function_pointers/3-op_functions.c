#include "cal.h"

/**
 * op_add - addition of two int
 * op_sub - substraction of two int
 * op_mul - multiplication of two int
 * op_div - division of two int
 * op_mod - remiander of two int
 * @a: int one
 * @b: int two
 * Return: Value
 */
int op_add(int a, int b)
{ 
	return (a + b);
}

int op_sub(int a, int b)
{
	return (a - b);
}

int op_mul(int a, int b)
{
	return (a * b);
}

int op_div(int a, int b)
{
	return (a / b);
}

int op_mod(int a, int b)
{
	return (a % b);
}
